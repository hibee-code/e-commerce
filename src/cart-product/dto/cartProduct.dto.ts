import {
  IsNotEmpty,
  IsNumber,
  IsNumberString,
  IsString,
} from 'class-validator';

export class CartProductDto {
  @IsNotEmpty()
  @IsNumberString()
  cartId: string;

  @IsNotEmpty()
  @IsNumberString()
  productId: string;

  @IsNotEmpty()
  @IsNumber()
  price: number;

  @IsNotEmpty()
  @IsNumber()
  quantity: number;

  @IsNotEmpty()
  @IsString()
  image: string;
}
